/* USER CODE BEGIN 1 */
void uartStartReceive(){
	for(uint8_t i = 0; i < RX_BUFFER_SIZE; i++)
	{
		rxBuffer[i] = 0;
	}
	rxIdxBuffer = isReady = 0;
	uartReceive();
}

void uartOnReceive(){
	if(rxIdxBuffer >= RX_BUFFER_SIZE)
	{
		return; // Buffer complete
	}
	memcpy(&(rxBuffer[rxIdxBuffer]), rxPacket, RX_PACKET_SIZE);
	rxIdxBuffer += RX_PACKET_SIZE;
	uartReceive();
}

void uartReceive()
{
	for(uint8_t i = 0; i < RX_PACKET_SIZE; i++)
	{
		rxPacket[i] = 0;
	}
	isReady = 0;
	__HAL_UART_ENABLE_IT(&huart2, UART_IT_IDLE);
	HAL_UART_Receive_DMA(&huart2, rxPacket, RX_PACKET_SIZE);
	__HAL_DMA_DISABLE_IT(huart2.hdmarx, DMA_IT_HT);
}

uint8_t uartIsReady(){
	return isReady != 0;
}

// TODO: Check digit implementation require app modification.
uint8_t uartCheckDigit(){
	return 1;
}

void uartSetReady(){
	// Whether dma transfer is incomplete
	if(hdma_usart2_rx.Instance->CNDTR < RX_PACKET_SIZE){
		HAL_UART_DMAStop(&huart2);
		uartOnReceive();
	}
	isReady = uartCheckDigit();
	if(!isReady)
	{
	  uartTransmit("RETRANSMIT");
	  uartStartReceive();
	}
}

uint8_t* uartGetData(){
	return rxBuffer;
}

void uartTransmit(uint8_t* data, uint8_t bcc){
	uint8_t len = strlen(data);
	txBuffer[0] = bcc;
	memcpy(&txBuffer[1], data, len + 1);
	HAL_UART_Transmit_DMA(&huart2, txBuffer, len + 2);
}


